    public function getClientsJson()
    {
        $clients = Client::all();
        $clientsSearchArray = $clients->map(function (Client $client) {
            $label = ['ID: ' . $client->id];
            if (!empty($client->enterprise_name)) {
                $label[] = $client->enterprise_name;
            }
            $label[] = $client->firstname . ' ' . $client->lastname;
            if (!empty($client->telephone)) {
                $label[] = $client->telephone;
            }
            if (!empty($client->email)) {
                $label[] = $client->email;
            }

            return $client->toArray() + ['label' => implode(', ', $label)];
        });
        return response()->json($clientsSearchArray->toArray());
    }
